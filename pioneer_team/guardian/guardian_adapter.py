def render_certificate_text(result: GuardianAdapterResult) -> str:
    valid_until = (datetime.now(timezone.utc) + timedelta(days=365)).date().isoformat()

    lines = [
        "AGENTIC ZERO - GUARDIAN CERTIFICATE",
        "=" * 42,
        f"Process ID:       {result.process_id}",
        f"Agent:            {result.agentic_certificate.agent_name}",
        f"Issued at:        {result.issued_at}",
        f"Valid until:      {valid_until}",
        f"Overall status:   {result.overall_status}",
        f"Overall score:    {pct(result.overall_score)}%",
        f"Delivery allowed: {result.delivery_allowed}",
        f"Human signoff:    {result.human_signoff_required}",
        "",
        "CHECK SUMMARY",
        "-" * 42,
    ]

    for check in result.checks:
        lines.extend(
            [
                f"{check.name}: {check.status} ({pct(check.score)}%)",
                *[f"  - {i}" for i in check.issues[:5]],
            ]
        )

    lines.extend(
        [
            "",
            "AGENTIC CERTIFICATE",
            "-" * 42,
            f"Learning ready:          {result.agentic_certificate.learning_ready}",
            f"Memory ready:            {result.agentic_certificate.memory_ready}",
            f"Prescriptive ready:      {result.agentic_certificate.prescriptive_ready}",
            f"Self-improvement ready:  {result.agentic_certificate.self_improvement_ready}",
            f"Machine compatible:      {result.agentic_certificate.machine_compatible}",
            f"Agentic score:           {pct(result.agentic_certificate.score)}%",
            "",
            "REMEDIATION PLAN",
            "-" * 42,
        ]
    )

    if result.remediation_plan:
        lines.extend([f"- {r}" for r in result.remediation_plan[:20]])
    else:
        lines.append("- No remediation required")

    lines.extend(
        [
            "",
            "MANTRA",
            "-" * 42,
            result.mantra,
        ]
    )

    return "\n".join(lines)


def save_outputs(
    pkg: dict[str, Any], result: GuardianAdapterResult
) -> GuardianAdapterResult:
    compliance_dir = pkg["root"] / "06_compliance"
    compliance_dir.mkdir(parents=True, exist_ok=True)

    guardian_result_path = compliance_dir / "guardian_result.json"
    guardian_certificate_path = compliance_dir / "guardian_certificate.txt"
    agentic_certificate_path = compliance_dir / "agentic_certificate.json"

    write_json(guardian_result_path, asdict(result))
    write_text(guardian_certificate_path, render_certificate_text(result))
    write_json(agentic_certificate_path, asdict(result.agentic_certificate))

    result.outputs = {
        "guardian_result_json": str(guardian_result_path),
        "guardian_certificate_txt": str(guardian_certificate_path),
        "agentic_certificate_json": str(agentic_certificate_path),
    }

    write_json(guardian_result_path, asdict(result))
    return result


