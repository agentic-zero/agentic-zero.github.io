def certify_essential_package(package_dir: str | Path) -> GuardianAdapterResult:
    pkg = load_essential_package(package_dir)
    checks = run_all_checks(pkg)
    agentic_cert = build_agentic_certificate(pkg, checks)

    (
        overall_status,
        overall_score,
        delivery_allowed,
        human_signoff_required,
        remediation,
    ) = decide_status(checks)

    if not agentic_cert.machine_compatible:
        if overall_status == "CERTIFIED":
            overall_status = "CONDITIONAL"
        human_signoff_required = True
        remediation.append(
            "agentic_certificate: complete Machine compatibility before autonomous learning activation"
        )

    result = GuardianAdapterResult(
        process_id=pkg["process_id"],
        package_dir=str(pkg["root"]),
        issued_at=_now(),
        overall_status=overall_status,
        overall_score=overall_score,
        delivery_allowed=delivery_allowed,
        human_signoff_required=human_signoff_required,
        checks=checks,
        agentic_certificate=agentic_cert,
        remediation_plan=remediation,
        outputs={},
        next_step="Run auditor.py / delivery_gate.py",
    )

    return save_outputs(pkg, result)


def run_cli(package_dir: str):
    result = certify_essential_package(package_dir)

    print("\nGuardian Adapter complete")
    print(f"Status: {result.overall_status}")
    print(f"Score:  {pct(result.overall_score)}%")
    print(f"Delivery allowed: {result.delivery_allowed}")
    print(f"Human signoff:    {result.human_signoff_required}")
    print("\nOutput:")
    for k, v in result.outputs.items():
        print(f"  {k}: {v}")
    print(f"\nNext: {result.next_step}")

    if result.remediation_plan:
        print("\nTop remediation:")
        for item in result.remediation_plan[:8]:
            print(f"  - {item}")

    return result


if __name__ == "__main__":
    parser = argparse.ArgumentParser(description="Agentic Zero - Guardian Adapter")
    parser.add_argument(
        "--package-dir", required=True, help="Path to customer essential_package folder"
    )
    args = parser.parse_args()
    run_cli(args.package_dir)
