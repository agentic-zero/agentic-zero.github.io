@dataclass
class CheckResult:
    name: str
    status: str  # PASS | WARN | FAIL
    score: float
    issues: list[str] = field(default_factory=list)
    recommendations: list[str] = field(default_factory=list)


@dataclass
class AgenticCertificate:
    certificate_id: str
    issued_at: str
    process_id: str
    agent_name: str
    learning_ready: bool
    memory_ready: bool
    prescriptive_ready: bool
    self_improvement_ready: bool
    machine_compatible: bool
    score: float
    issues: list[str]
    recommendations: list[str]


@dataclass
class GuardianAdapterResult:
    process_id: str
    package_dir: str
    issued_at: str
    overall_status: str  # CERTIFIED | CONDITIONAL | REJECTED
    overall_score: float
    delivery_allowed: bool
    human_signoff_required: bool
    checks: list[CheckResult]
    agentic_certificate: AgenticCertificate
    remediation_plan: list[str]
    outputs: dict[str, str]
    next_step: str
    mantra: str = "Does this make it feel like a living enterprise?"


# ---------------------------------------------------------------------------
# UTILS
# ---------------------------------------------------------------------------


def _now() -> str:
    return datetime.now(timezone.utc).isoformat()


def _slug(value: str) -> str:
    value = (value or "process").lower().strip()
    value = re.sub(r"[^a-z0-9]+", "_", value)
    return value.strip("_") or "process"


def read_json(
    path: str | Path, default: Optional[dict[str, Any]] = None
) -> dict[str, Any]:
    path = Path(path)
    if not path.exists():
        return default or {}
    with open(path, encoding="utf-8") as f:
        return json.load(f)


def write_json(path: str | Path, payload: dict[str, Any]) -> Path:
    path = Path(path)
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(json.dumps(payload, indent=2, ensure_ascii=False), encoding="utf-8")
    return path


def write_text(path: str | Path, text: str) -> Path:
    path = Path(path)
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(text, encoding="utf-8")
    return path


def exists(path: str | Path) -> bool:
    return Path(path).exists()


def pct(score: float) -> int:
    return int(round(score * 100))


