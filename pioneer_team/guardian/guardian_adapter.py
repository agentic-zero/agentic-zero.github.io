def build_agentic_certificate(
    pkg: dict[str, Any], checks: list[CheckResult]
) -> AgenticCertificate:
    hooks = (
        pkg["blueprint"].get("learning_hooks")
        or pkg["siop"].get("learning_hooks")
        or {}
    )

    learning_ready = bool(hooks)
    memory_ready = bool(
        hooks.get("observation_points") or hooks.get("failure_patterns")
    )
    prescriptive_ready = bool(
        hooks.get("improvement_loop") or hooks.get("feedback_targets")
    )
    self_improvement_ready = bool(
        hooks.get("kpi_deviation_signals") and hooks.get("feedback_targets")
    )
    machine_compatible = all(
        [
            learning_ready,
            memory_ready,
            prescriptive_ready,
            self_improvement_ready,
        ]
    )

    score_items = [
        learning_ready,
        memory_ready,
        prescriptive_ready,
        self_improvement_ready,
        machine_compatible,
    ]
    score = sum(1 for x in score_items if x) / len(score_items)

    issues = []
    recommendations = []

    if not learning_ready:
        issues.append("Learning hooks missing")
    if not memory_ready:
        issues.append("Memory observation points missing")
    if not prescriptive_ready:
        issues.append("Prescriptive feedback loop missing")
    if not self_improvement_ready:
        issues.append("Self-improvement signals incomplete")
    if not machine_compatible:
        recommendations.append("Complete hooks before connecting to The Machine")

    return AgenticCertificate(
        certificate_id=f"AZ-AGENTIC-{pkg['process_id']}-{datetime.now().strftime('%Y%m%d')}",
        issued_at=_now(),
        process_id=pkg["process_id"],
        agent_name=pkg["agent_name"],
        learning_ready=learning_ready,
        memory_ready=memory_ready,
        prescriptive_ready=prescriptive_ready,
        self_improvement_ready=self_improvement_ready,
        machine_compatible=machine_compatible,
        score=round(score, 2),
        issues=issues,
        recommendations=recommendations,
    )


