def check_artifact_completeness(pkg: dict[str, Any]) -> CheckResult:
    required = [
        "functional",
        "siop",
        "siop_validation",
        "blueprint",
        "agent_runtime",
        "sop",
        "integration_guide",
        "escalation_policy",
        "client_summary",
        "dashboard",
        "roi",
        "delivery_manifest",
    ]

    missing = [k for k in required if not pkg["paths"][k].exists()]
    score = max(0.0, 1.0 - (len(missing) * 0.08))

    status = "PASS" if not missing else ("WARN" if score >= 0.70 else "FAIL")

    return CheckResult(
        name="artifact_completeness",
        status=status,
        score=round(score, 2),
        issues=[f"Missing artifact: {m}" for m in missing],
        recommendations=["Generate missing artifacts before delivery"]
        if missing
        else [],
    )


def check_eu_ai_act(pkg: dict[str, Any]) -> CheckResult:
    text = json.dumps(
        {
            "functional": pkg["functional"],
            "siop": pkg["siop"],
            "blueprint": pkg["blueprint"],
        },
        ensure_ascii=False,
    ).lower()

    prohibited_terms = [
        "social scoring",
        "mass surveillance",
        "subliminal manipulation",
        "exploit vulnerabilities",
    ]

    high_risk_terms = [
        "biometric",
        "law enforcement",
        "medical",
        "pharma",
        "defense",
        "creditworthiness",
        "worker evaluation",
        "recruitment",
        "critical infrastructure",
    ]

    issues = []
    recommendations = []

    if any(t in text for t in prohibited_terms):
        return CheckResult(
            name="eu_ai_act",
            status="FAIL",
            score=0.0,
            issues=["Possible prohibited AI use case detected"],
            recommendations=["Human legal review required before continuing"],
        )

    risk_level = "minimal"
    if any(t in text for t in high_risk_terms):
        risk_level = "high"
        issues.append("High-risk indicators detected")
        recommendations.extend(
            [
                "Document EU AI Act Art.9 risk management system",
                "Document Art.10 data governance",
                "Document Art.11 technical documentation",
                "Document Art.12 logging",
                "Document Art.14 human oversight",
            ]
        )

    blueprint = pkg["blueprint"]
    shield = blueprint.get("shield_requirements", [])
    audit_ok = any("audit" in str(s).lower() for s in shield) or "audit" in text
    human_ok = (
        any("human" in str(s).lower() for s in shield) or "human oversight" in text
    )

    if not audit_ok:
        issues.append("EU AI Act logging/audit trail not explicit")
        recommendations.append("Add explicit Art.12 audit trail requirement")

    if not human_ok:
        issues.append("EU AI Act human oversight not explicit")
        recommendations.append("Add explicit Art.14 human oversight checkpoint")

    if risk_level == "high":
        score = 0.75 if audit_ok and human_ok else 0.62
        status = "WARN"
    else:
        score = 0.92 if audit_ok and human_ok else 0.78
        status = "PASS" if score >= 0.85 else "WARN"

    return CheckResult(
        name="eu_ai_act",
        status=status,
        score=round(score, 2),
        issues=issues,
        recommendations=recommendations,
    )


def check_gdpr(pkg: dict[str, Any]) -> CheckResult:
    text = json.dumps(
        {
            "functional": pkg["functional"],
            "siop": pkg["siop"],
            "blueprint": pkg["blueprint"],
        },
        ensure_ascii=False,
    ).lower()

    personal_terms = [
        "customer",
        "email",
        "name",
        "address",
        "employee",
        "contact",
        "phone",
        "personal",
        "id",
    ]

    personal_data = any(t in text for t in personal_terms)
    issues = []
    recommendations = []

    if personal_data:
        issues.append("Personal or contact data likely involved")
        recommendations.extend(
            [
                "Define lawful basis for processing",
                "Confirm data minimization",
                "Confirm retention policy",
                "Ensure audit trail for personal data access",
            ]
        )

    score = 0.82 if personal_data else 0.95
    status = "WARN" if personal_data else "PASS"

    return CheckResult(
        name="gdpr",
        status=status,
        score=score,
        issues=issues,
        recommendations=recommendations,
    )


def check_iso_42001(pkg: dict[str, Any]) -> CheckResult:
    blueprint = pkg["blueprint"]
    siop = pkg["siop"]

    issues = []
    recommendations = []

    has_risk = bool(siop.get("compliance") or blueprint.get("compliance_frameworks"))
    has_monitoring = bool(blueprint.get("kpis") or siop.get("acceptance_criteria"))
    has_improvement = bool(
        blueprint.get("learning_hooks") or siop.get("learning_hooks")
    )

    if not has_risk:
        issues.append("AI risk/compliance context not explicit")
    if not has_monitoring:
        issues.append("AI performance monitoring not explicit")
    if not has_improvement:
        issues.append("Continual improvement / learning hooks missing")

    recommendations.extend(
        [
            "Map AI risks to ISO/IEC 42001 planning controls",
            "Keep KPI monitoring evidence",
            "Register improvement actions through The Machine",
        ]
    )

    score = 1.0
    if not has_risk:
        score -= 0.18
    if not has_monitoring:
        score -= 0.18
    if not has_improvement:
        score -= 0.18

    status = "PASS" if score >= 0.85 else ("WARN" if score >= 0.65 else "FAIL")

    return CheckResult(
        name="iso_42001",
        status=status,
        score=round(score, 2),
        issues=issues,
        recommendations=recommendations if issues else [],
    )


def check_nist_ai_rmf(pkg: dict[str, Any]) -> CheckResult:
    blueprint = pkg["blueprint"]
    siop = pkg["siop"]

    govern = bool(blueprint.get("shield_requirements") or siop.get("compliance"))
    map_ = bool(
        siop.get("business_context") or pkg["functional"].get("business_context")
    )
    measure = bool(blueprint.get("kpis") or siop.get("acceptance_criteria"))
    manage = bool(blueprint.get("escalations") or siop.get("autonomy_design"))

    checks = {
        "govern": govern,
        "map": map_,
        "measure": measure,
        "manage": manage,
    }

    missing = [k for k, v in checks.items() if not v]
    score = sum(1 for v in checks.values() if v) / 4

    return CheckResult(
        name="nist_ai_rmf",
        status="PASS" if score >= 0.85 else ("WARN" if score >= 0.60 else "FAIL"),
        score=round(score, 2),
        issues=[f"NIST function incomplete: {m}" for m in missing],
        recommendations=["Complete NIST AI RMF Govern/Map/Measure/Manage evidence"]
        if missing
        else [],
    )


def check_agentic_shield(pkg: dict[str, Any]) -> CheckResult:
    blueprint = pkg["blueprint"]
    shield = " ".join(str(x) for x in blueprint.get("shield_requirements", [])).lower()

    required = {
        "identity": ["identity", "access"],
        "thresholds": ["threshold"],
        "audit": ["audit", "log"],
        "escalation": ["escalation"],
        "fail_safe": ["fail", "safe", "rollback"],
        "human_accountability": ["human", "accountability", "oversight"],
    }

    missing = []
    for key, terms in required.items():
        if not any(t in shield for t in terms):
            missing.append(key)

    score = max(0.0, 1.0 - len(missing) * 0.12)
    status = "PASS" if not missing else ("WARN" if score >= 0.70 else "FAIL")

    return CheckResult(
        name="agentic_shield",
        status=status,
        score=round(score, 2),
        issues=[f"Missing Shield control: {m}" for m in missing],
        recommendations=["Add missing Shield controls to ArchitectBlueprint"]
        if missing
        else [],
    )


def check_living_enterprise(pkg: dict[str, Any]) -> CheckResult:
    hooks = (
        pkg["blueprint"].get("learning_hooks")
        or pkg["siop"].get("learning_hooks")
        or {}
    )

    required = [
        "observation_points",
        "failure_patterns",
        "kpi_deviation_signals",
        "feedback_targets",
        "improvement_loop",
    ]

    missing = [k for k in required if not hooks.get(k)]
    score = max(0.0, 1.0 - len(missing) * 0.16)
    status = "PASS" if not missing else ("WARN" if score >= 0.60 else "FAIL")

    return CheckResult(
        name="living_enterprise",
        status=status,
        score=round(score, 2),
        issues=[f"Missing learning hook: {m}" for m in missing],
        recommendations=[
            "Complete learning hooks so The Machine can learn from this agent"
        ]
        if missing
        else [],
    )


def run_all_checks(pkg: dict[str, Any]) -> list[CheckResult]:
    return [
        check_artifact_completeness(pkg),
        check_eu_ai_act(pkg),
        check_gdpr(pkg),
        check_iso_42001(pkg),
        check_nist_ai_rmf(pkg),
        check_agentic_shield(pkg),
        check_living_enterprise(pkg),
    ]


