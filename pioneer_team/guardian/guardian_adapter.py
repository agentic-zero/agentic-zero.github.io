def decide_status(
    checks: list[CheckResult],
) -> tuple[str, float, bool, bool, list[str]]:
    scores = [c.score for c in checks]
    overall_score = round(sum(scores) / len(scores), 2) if scores else 0.0

    fails = [c for c in checks if c.status == "FAIL"]
    warns = [c for c in checks if c.status == "WARN"]

    remediation = []
    for c in checks:
        remediation.extend([f"{c.name}: {i}" for i in c.issues])
        remediation.extend([f"{c.name}: {r}" for r in c.recommendations])

    if fails:
        return "REJECTED", overall_score, False, True, remediation

    if warns or overall_score < 0.88:
        return "CONDITIONAL", overall_score, True, True, remediation

    return "CERTIFIED", overall_score, True, False, remediation


